#include "ft_header.h"

int		ft_isalnum(int ch)
{
	return ((((char)ch >= 'a' && (char)ch <= 'z') ||
						((char)ch >= 'A' && (char)ch <= 'Z') ||
								((char)ch >= '0' && (char)ch <= '9')) ? 1 : 0);
}
